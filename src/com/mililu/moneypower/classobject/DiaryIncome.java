package com.mililu.moneypower.classobject;

public class DiaryIncome {
	private int id_diary_income;
	private int id_income;
	private int id_wallet;
	private String date;
	private String time;
	private int money;
	private String notice;
	public int getId_diary_income() {
		return id_diary_income;
	}
	public void setId_diary_income(int id_diary_income) {
		this.id_diary_income = id_diary_income;
	}
	public int getId_income() {
		return id_income;
	}
	public void setId_income(int id_income) {
		this.id_income = id_income;
	}
	public int getId_wallet() {
		return id_wallet;
	}
	public void setId_wallet(int id_wallet) {
		this.id_wallet = id_wallet;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public String getNotice() {
		return notice;
	}
	public void setNotice(String notice) {
		this.notice = notice;
	}

	
}
