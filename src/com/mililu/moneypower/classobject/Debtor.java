package com.mililu.moneypower.classobject;

public class Debtor {
	int id_debtor;
	String name_debtor;
	int id_account;
	public int getId_debtor() {
		return id_debtor;
	}
	public void setId_debtor(int id_debtor) {
		this.id_debtor = id_debtor;
	}
	public String getName_debtor() {
		return name_debtor;
	}
	public void setName_debtor(String name_debtor) {
		this.name_debtor = name_debtor;
	}
	public int getId_account() {
		return id_account;
	}
	public void setId_account(int id_account) {
		this.id_account = id_account;
	}
	
}
