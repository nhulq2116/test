package com.mililu.moneypower.classobject;

public class ExpenditureDetail {
	int id_exp_det;
	int id_exp;
	String name_expend;
	public int getId_exp_det() {
		return id_exp_det;
	}
	public void setId_exp_det(int id_exp_det) {
		this.id_exp_det = id_exp_det;
	}
	public int getId_exp() {
		return id_exp;
	}
	public void setId_exp(int id_exp) {
		this.id_exp = id_exp;
	}
	public String getName_expend() {
		return name_expend;
	}
	public void setName_expend(String name_expend) {
		this.name_expend = name_expend;
	}
}
